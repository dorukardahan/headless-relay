#!/bin/sh
# test-grok-runtime.sh — RUNTIME proof of the M6 (hermetic) isolation in grok_relay / grok_media.
# ---------------------------------------------------------------------------------------
# Runtime counterpart to scripts/regression-grok-safety.sh (a STATIC text tripwire). This script:
#   1. Extracts the REAL grok_relay/grok_media from ../SKILL.md at runtime (by pattern, never by
#      line number) so it always tests the shipped code.
#   2. Generates a self-contained FAKE `grok` and puts it FIRST on a minimal PATH that EXCLUDES the
#      dirs a real grok lives in (~/.local/bin, ~/.grok/bin, homebrew, npm-global). The real grok /
#      network / xAI is NEVER touched. Because the helper's `env -i` strips env vars, the fake cannot
#      rely on an env sentinel — it self-identifies via a BAKED-IN absolute path and a FILE sentinel,
#      and REFUSES to run (exit 99) if that sentinel is absent (so a stray copy on a real PATH screams
#      instead of shadowing grok). A safety gate re-asserts `command -v grok` == the fake before every
#      scenario; if it ever resolves elsewhere the run ABORTS.
#   3. Runs a full isolation / lifecycle / publish matrix under sh + bash + zsh.
#   4. Proves each protection with mutation red-green testing on a COPY of the helpers (SKILL.md is
#      never modified).
#
# Everything runs under a private temp tree; the real ~/.grok, ~/.claude and the installed skill
# copies are never read or written. Synthetic data only; no token/key VALUE is ever written to a log.
#
# Exit 0 iff every scenario passed on every available shell AND every mutation was caught (red) while
# pristine passed (green). Missing shells are SKIPPED. NOTE: this proves the WRAPPER's isolation; it
# does NOT and cannot verify the shipped grok binary (unverifiable by design; see SECURITY.md).

set -u

# --------------------------------------------------------------------------- paths & setup
SELF=$0; case "$SELF" in /*) ;; *) SELF="$(pwd)/$SELF" ;; esac
SCRIPT_DIR=$(CDPATH= cd -- "$(dirname -- "$SELF")" && pwd)
REPO_ROOT=$(CDPATH= cd -- "$SCRIPT_DIR/.." && pwd)
SKILL="$REPO_ROOT/SKILL.md"
[ -f "$SKILL" ] || { echo "FATAL: SKILL.md not found at $SKILL" >&2; exit 2; }

_tmpbase=${TMPDIR:-/tmp}; _tmpbase=${_tmpbase%/}
WORK=$(mktemp -d "$_tmpbase/grok-runtime-test.XXXXXX") || { echo "FATAL: mktemp" >&2; exit 2; }
cleanup() { chmod -R u+rwx "$WORK" 2>/dev/null; rm -rf "$WORK" 2>/dev/null; }
trap 'cleanup' EXIT INT TERM HUP

FAKEBIN="$WORK/bin"; LOGDIR="$WORK/log"; DRIVER="$WORK/driver.sh"; PRISTINE="$WORK/helpers.pristine.sh"
RESULTS="$WORK/results.txt"; MUTLOG="$WORK/mutations.txt"
mkdir -p "$FAKEBIN" "$LOGDIR" "$WORK/runs" || { echo "FATAL: mkdir" >&2; exit 2; }
: > "$RESULTS"; : > "$MUTLOG"

# Minimal PATH: fake FIRST, then system dirs ONLY. Every dir a real grok could live in is excluded.
SAFEPATH="/usr/bin:/bin:/usr/sbin:/sbin"
CHILD_PATH="$FAKEBIN:$SAFEPATH"

# ------------------------------------------------------------------- extract shipped helpers
extract_fn() { awk -v fn="$1" '$0 ~ ("^" fn "\\(\\) \\($") {f=1} f{print} f && $0==")"{exit}' "$SKILL"; }
{ extract_fn grok_relay; echo; extract_fn grok_media; } > "$PRISTINE"
_hr=$(grep -c '^grok_relay() ($' "$PRISTINE"); _hm=$(grep -c '^grok_media() ($' "$PRISTINE")
if [ "$_hr" != 1 ] || [ "$_hm" != 1 ]; then
  echo "FATAL: could not extract exactly one grok_relay and one grok_media (relay=$_hr media=$_hm)" >&2; exit 2
fi
sh -n "$PRISTINE" 2>"$WORK/synerr" || { echo "FATAL: extracted helpers do not parse: $(cat "$WORK/synerr")" >&2; exit 2; }
for anchor in 'env -i' '\-\-deny' 'image_gen' 'GROK_AUTH_PATH' 'mktemp -d' 'trap ' 'set +eux' 'ln "$curtmp"' 'NO-HARDLINK-FAILCLOSED' 'manifest0' 'pending=' 'sbxreal' '_reappg' 'pgok' 'ps -o pgid=' 'case "$brief$out"' 'pwd -P' '_rollback' 'could not write config' '_ingit'; do
  grep -q "$anchor" "$PRISTINE" || { echo "FATAL: extracted helpers missing anchor: $anchor" >&2; exit 2; }
done

# --------------------------------------------------------------------------- generate fake grok
cat > "$FAKEBIN/grok.tmpl" <<'FAKE_EOF'
#!/bin/sh
# FAKE grok — generated by test-grok-runtime.sh. NOT the real grok. Makes no network call. Refuses to
# run unless the harness FILE sentinel is present (survives env -i, which strips env sentinels).
L='__LOGDIR__'
if [ ! -f "$L/SENTINEL" ]; then
  echo "FATAL fake-grok: sentinel absent at $L — refusing to run (guards a real grok invocation or PATH leak)." >&2
  exit 99
fi
printf '%s' "$$" > "$L/fake.pid"
# Adversarial: a temp-ENUMERATING malicious binary finds the wrapper's control base(s) (grok-ctl.*,
# which it was never given a path to) under the temp root and tampers with control/coordination files.
_troot=$(dirname "$(dirname "${TMPDIR:-/x/y}")")
if [ "$(cat "$L/spoof" 2>/dev/null || echo 0)" = 1 ]; then
  for _c in "$_troot"/grok-ctl.*; do [ -d "$_c" ] || continue; : > "$_c/done" 2>/dev/null; : > "$_c/timedout" 2>/dev/null; done
fi
_atk=$(cat "$L/attack" 2>/dev/null || echo none)
if [ "$_atk" != none ]; then
  for _c in "$_troot"/grok-ctl.*; do
    [ -d "$_c" ] || continue
    case "$_atk" in
      out_symlink)      rm -f "$_c/out" 2>/dev/null;      ln -s "$L/CANARY" "$_c/out" 2>/dev/null ;;
      out_fifo)         rm -f "$_c/out" 2>/dev/null;      mkfifo "$_c/out" 2>/dev/null ;;
      manifest_symlink) rm -f "$_c/manifest" 2>/dev/null; ln -s "$L/CANARY" "$_c/manifest" 2>/dev/null ;;
      manifest_fifo)    rm -f "$_c/manifest" 2>/dev/null; mkfifo "$_c/manifest" 2>/dev/null ;;
    esac
  done
fi
F="$L/$$.env"
isset() { eval "_v=\${$1+x}"; [ -n "${_v:-}" ] && printf '%s=set\n' "$1" || printf '%s=unset\n' "$1"; }
val()   { eval "_v=\${$1:-<UNSET>}"; printf '%s=%s\n' "$1" "$_v"; }
{
  echo "MARKER=FAKEGROK"
  val HOME; val GROK_HOME; val TMPDIR; val TERM; val PATH; val GROK_AUTH_PATH
  val GROK_TELEMETRY_ENABLED; val GROK_TELEMETRY_TRACE_UPLOAD; val GROK_EXTERNAL_OTEL
  # secrets: report set/unset only (never write a key VALUE to the log)
  isset XAI_API_KEY; isset GROK_CODE_XAI_API_KEY; isset GROK_AUTH; isset XAI_ROOT; isset XAI_USER
  isset GROK_DISABLE_API_KEY_AUTH
  # canaries that MUST NOT reach the child (set/unset)
  isset CANARY_SECRET; isset AWS_SECRET_ACCESS_KEY; isset GITHUB_TOKEN
  isset GROK_XAI_API_BASE_URL; isset XAI_API_BASE_URL; isset GROK_GATEWAY_URL; isset GROK_CLI_CHAT_PROXY_BASE_URL
  isset GROK_AUTH_PROVIDER_COMMAND; isset GROK_LOG_FILE; isset GROK_MANAGED_CONFIG; isset GROK_TRACE_UPLOAD_URL
  isset GROK_CLAUDE_SKILLS_ENABLED; isset GROK_CODEX_SESSIONS_ENABLED; isset GROK_FOLDER_TRUST
  echo "WSDC_VAL=${GROK_WORKSPACE_DATA_COLLECTION_DISABLED:-<UNSET>}"; isset GROK_SESSION_RECAP; isset GROK_FEEDBACK_ENABLED
  echo "PWD=$(pwd)"
  git rev-parse --is-inside-work-tree >/dev/null 2>&1 && echo "IN_GIT=yes" || echo "IN_GIT=no"
  [ -e "${HOME:-/nope}/.claude" ] && echo "HOME_CLAUDE=yes" || echo "HOME_CLAUDE=no"
  [ -e "${HOME:-/nope}/.codex" ]  && echo "HOME_CODEX=yes"  || echo "HOME_CODEX=no"
  [ -e "${GROK_HOME:-/nope}/AGENTS.md" ] && echo "GH_AGENTS=yes" || echo "GH_AGENTS=no"
  cfg="${GROK_HOME:-/nope}/config.toml"
  if [ -f "$cfg" ]; then
    grep -q 'telemetry = false' "$cfg"  && echo "CFG_TELEMETRY=yes" || echo "CFG_TELEMETRY=no"
    grep -q 'trace_upload = false' "$cfg" && echo "CFG_TRACE=yes" || echo "CFG_TRACE=no"
    grep -q '\[compat.claude\]' "$cfg" && echo "CFG_CLAUDE=yes" || echo "CFG_CLAUDE=no"
    grep -q '\[compat.cursor\]' "$cfg" && echo "CFG_CURSOR=yes" || echo "CFG_CURSOR=no"
    grep -q '\[compat.codex\]' "$cfg"  && echo "CFG_CODEX=yes" || echo "CFG_CODEX=no"
    grep -q '^auto_update = false' "$cfg" && echo "CFG_AUTOUPDATE=yes" || echo "CFG_AUTOUPDATE=no"
    grep -q '^use_leader = false' "$cfg"  && echo "CFG_USELEADER=yes" || echo "CFG_USELEADER=no"
    printf 'CFG_SKILLS_FALSE_N=%s\n' "$(grep -c 'skills = false' "$cfg")"
    printf 'CFG_COMPAT_FALSE_N=%s\n' "$(grep -cE '^(skills|rules|agents|mcps|hooks|sessions) = false' "$cfg")"
  else echo "CFG_MISSING=yes"; fi
  echo "ARGV_BEGIN"; for _a in "$@"; do printf '%s\n' "$_a"; done; echo "ARGV_END"
} >> "$F"
# per-call overrides embedded in the prompt/brief argv (so concurrent calls can differ with one fake)
_rcovr=; _slpovr=; _amovr=
for _a in "$@"; do
  case "$_a" in *RCOVR=*)     _t=${_a##*RCOVR=};     _rcovr=${_t%%[!0-9]*} ;; esac
  case "$_a" in *SLEEPOVR=*)   _t=${_a##*SLEEPOVR=};  _slpovr=${_t%%[!0-9]*} ;; esac
  case "$_a" in *ARTMODE=*)    _t=${_a##*ARTMODE=};   _amovr=${_t%%[!a-z]*} ;; esac
done
# optional grandchild (for process-tree kill tests); duration overridable (short for the normal-exit limit demo)
[ "$(cat "$L/grandchild" 2>/dev/null || echo 0)" = 1 ] && { sleep "$(cat "$L/gcdur" 2>/dev/null || echo 55)" & printf '%s' "$!" > "$L/gc.pid"; }
# optional long turn (for timeout / signal tests)
_s=$(cat "$L/sleep" 2>/dev/null || echo 0); [ -n "$_slpovr" ] && _s=$_slpovr; [ "$_s" = 0 ] || sleep "$_s"
# media vs text
_media=0; for _a in "$@"; do [ "$_a" = "--tools" ] && _media=1; done
if [ "$_media" = 1 ]; then
  _d="${GROK_HOME:-.}/sessions/enc-$$"; mkdir -p "$_d/images"
  _am=$(cat "$L/artmode" 2>/dev/null || echo normal); [ -n "$_amovr" ] && _am=$_amovr
  case "$_am" in
    evil)  printf 'IMG\n' > "$_d/images/good.jpg"; _nm=$(printf 'decoy\nstealme.jpg'); : > "$_d/images/$_nm" 2>/dev/null ;;
    space) printf 'IMG\n' > "$_d/images/my art (v2).jpg" ;;
    *) _ic=$(cat "$L/imgcount" 2>/dev/null || echo 1); _vc=$(cat "$L/vidcount" 2>/dev/null || echo 0)
       _n=1; while [ "$_n" -le "$_ic" ] 2>/dev/null; do printf 'IMG-%s\n' "$_n" > "$_d/images/$_n.jpg"; _n=$((_n+1)); done
       _n=1; while [ "$_n" -le "$_vc" ] 2>/dev/null; do mkdir -p "$_d/videos"; printf 'VID-%s\n' "$_n" > "$_d/videos/$_n.mp4"; _n=$((_n+1)); done ;;
  esac
else
  echo "FAKE-GROK-ANSWER ok"
fi
_rc=$(cat "$L/rc" 2>/dev/null || echo 0); [ -n "$_rcovr" ] && _rc=$_rcovr; exit "$_rc"
FAKE_EOF
sed "s|__LOGDIR__|$LOGDIR|g" "$FAKEBIN/grok.tmpl" > "$FAKEBIN/grok"; chmod +x "$FAKEBIN/grok"; rm -f "$FAKEBIN/grok.tmpl"
: > "$LOGDIR/SENTINEL"
printf 'CANARY-CONTENT-DO-NOT-LEAK\n' > "$LOGDIR/CANARY"   # target for control-path symlink attacks

# safety gate at harness level
_resolved=$(PATH="$CHILD_PATH" command -v grok 2>/dev/null || true)
[ "$_resolved" = "$FAKEBIN/grok" ] || { echo "FATAL: 'grok' on the test PATH resolves to '$_resolved', not the fake. Aborting." >&2; exit 2; }

# ------------------------------------------------------------------------------ generate driver
cat > "$DRIVER" <<'DRIVER_EOF'
#!/bin/sh
SCN=${1:-}
setopt NO_NOMATCH 2>/dev/null || :   # zsh: a glob with no match expands to nothing (not a fatal error)
: "${WORK:?}"; : "${FAKEBIN:?}"; : "${LOGDIR:?}"; : "${HELPERS_FILE:?}"

# SAFETY (load-bearing): re-assert a minimal PATH with the fake FIRST, from INSIDE the script body —
# after any shell startup file (zsh sources ~/.zshenv even for `zsh script`, and a real ~/.zshenv
# commonly PREPENDS ~/.local/bin where the real grok lives). Every dir a real grok could live in is
# excluded, so `grok` can ONLY ever resolve to the fake. The gate then REFUSES to run if that fails.
PATH="$FAKEBIN:/usr/bin:/bin:/usr/sbin:/sbin"; export PATH
_g=$(command -v grok 2>/dev/null || true)
[ "$_g" = "$FAKEBIN/grok" ] || { echo "FAIL: SAFETY GATE — grok resolves to '$_g', not the fake; refusing"; exit 1; }

# scrub any real auth/key vars so branch selection is ours
unset XAI_API_KEY GROK_CODE_XAI_API_KEY GROK_AUTH_PATH GROK_AUTH XAI_ROOT XAI_USER GROK_HOME 2>/dev/null || true

run=$(mktemp -d "$WORK/runs/r.XXXXXX") || { echo "FAIL: mktemp run dir"; exit 1; }
realhome="$run/realhome"
export TMPDIR="$run/tmp"; mkdir -p "$TMPDIR"
# reset fake control files
printf 0 > "$LOGDIR/rc"; printf 0 > "$LOGDIR/sleep"; printf 1 > "$LOGDIR/imgcount"; printf 0 > "$LOGDIR/vidcount"; printf 0 > "$LOGDIR/grandchild"; printf 55 > "$LOGDIR/gcdur"; printf normal > "$LOGDIR/artmode"; printf 0 > "$LOGDIR/spoof"; printf none > "$LOGDIR/attack"; printf 'CANARY-CONTENT-DO-NOT-LEAK\n' > "$LOGDIR/CANARY"; rm -f "$LOGDIR/gc.pid" "$LOGDIR/fake.pid"
# clear per-call env logs (guard glob for zsh nomatch)
for _e in "$LOGDIR"/*.env; do [ -e "$_e" ] && rm -f "$_e"; done

. "$HELPERS_FILE" || { echo "FAIL: cannot source helpers"; exit 1; }

fail() { printf 'FAIL: %s\n' "$*"; exit 1; }
ok()   { printf 'PASS\n'; exit 0; }
skip() { printf 'SKIP: %s\n' "$*"; exit 2; }
# Can this shell put a backgrounded job in its OWN process group? sh/bash: yes. zsh rejects `set -m`
# non-interactively; dash turns job control off. This decides whether the wrapper can reap an ordinary
# descendant that survives grok's NORMAL/NONZERO exit (reparented to init but still in grok's group).
if ( set -m ) 2>/dev/null; then PGOK=1; else PGOK=0; fi
setup_home() {
  mkdir -p "$realhome/.grok/sessions" "$realhome/.claude" "$realhome/.codex"
  printf '{"xai":{"access_token":"CANARY-%s-DO-NOT-COPY"}}\n' "$(basename "$run")" > "$realhome/.grok/auth.json"
  printf 'REAL-CLAUDE-RULES\n' > "$realhome/.claude/CLAUDE.md"
  printf 'REAL-GROK-AGENTS\n'  > "$realhome/.grok/AGENTS.md"
  printf 'REAL-CODEX\n'        > "$realhome/.codex/AGENTS.md"
  export HOME="$realhome"
}
E() { for _x in "$LOGDIR"/*.env; do [ -e "$_x" ] && echo "$_x"; done | sort | tail -1; }
lg() { awk -F= -v k="$1" '$1==k{sub(/^[^=]*=/,"");print;exit}' "$(E)" 2>/dev/null; }
fake_ran() { for _x in "$LOGDIR"/*.env; do [ -e "$_x" ] && return 0; done; return 1; }
nfiles() { find "$1" -type f 2>/dev/null | wc -l | tr -d ' '; }
mkbrief() { _b="$run/brief-$1.md"; printf 'generate an %s asset; save under the cwd\n' "$1" > "$_b"; printf '%s' "$_b"; }

case "$SCN" in

text_happy)
  setup_home
  out=$(grok_relay "hello world" 2>"$run/err"); rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc err=[$(cat "$run/err")]"
  fake_ran || fail "fake did not run"
  printf '%s' "$out" | grep -q 'FAKE-GROK-ANSWER' || fail "no answer on stdout: [$out]"
  [ "$(lg IN_GIT)" = no ] || fail "child ran inside git"
  ok ;;

media_happy)
  setup_home
  out=$(grok_media "$(mkbrief image)" "$run/out" 2>"$run/err"); rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc err=[$(cat "$run/err")]"
  fake_ran || fail "fake did not run"
  c=$(nfiles "$run/out"); [ "$c" = 1 ] || fail "expected 1 artifact, got $c"
  printf '%s' "$out" | grep -q "^$run/out/" || fail "published path not under out: [$out]"
  ok ;;

canary_env)
  setup_home
  export CANARY_SECRET=leak-me AWS_SECRET_ACCESS_KEY=aws-leak GITHUB_TOKEN=gh-leak \
    GROK_XAI_API_BASE_URL=https://evil XAI_API_BASE_URL=https://evil2 GROK_GATEWAY_URL=https://evil3 \
    GROK_CLI_CHAT_PROXY_BASE_URL=https://evil4 GROK_AUTH_PROVIDER_COMMAND=/evil/cmd GROK_LOG_FILE=/evil/log \
    GROK_MANAGED_CONFIG=/evil/mc GROK_TRACE_UPLOAD_URL=https://evil5 GROK_CLAUDE_SKILLS_ENABLED=true \
    GROK_CODEX_SESSIONS_ENABLED=true GROK_FOLDER_TRUST=1 \
    GROK_SESSION_RECAP=1 GROK_FEEDBACK_ENABLED=1 GROK_AUTH=inline-leak XAI_ROOT=/leak XAI_USER=leaker
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc err=[$(cat "$run/err")]"
  for v in CANARY_SECRET AWS_SECRET_ACCESS_KEY GITHUB_TOKEN GROK_XAI_API_BASE_URL XAI_API_BASE_URL \
           GROK_GATEWAY_URL GROK_CLI_CHAT_PROXY_BASE_URL GROK_AUTH_PROVIDER_COMMAND GROK_LOG_FILE \
           GROK_MANAGED_CONFIG GROK_TRACE_UPLOAD_URL GROK_CLAUDE_SKILLS_ENABLED GROK_CODEX_SESSIONS_ENABLED \
           GROK_FOLDER_TRUST GROK_SESSION_RECAP GROK_FEEDBACK_ENABLED \
           GROK_AUTH XAI_ROOT XAI_USER; do
    [ "$(lg "$v")" = unset ] || fail "SECURITY: $v reached the child (=$(lg "$v"))"
  done
  ok ;;

custom_grok_home)
  setup_home; mkdir -p "$run/customgrok"; cp "$realhome/.grok/auth.json" "$run/customgrok/auth.json"
  export GROK_HOME="$run/customgrok"; unset GROK_AUTH_PATH
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc err=[$(cat "$run/err")]"
  [ "$(lg GROK_AUTH_PATH)" = "$run/customgrok/auth.json" ] || fail "auth not resolved via custom GROK_HOME: $(lg GROK_AUTH_PATH)"
  ok ;;

separate_dirs)
  setup_home
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc err=[$(cat "$run/err")]"
  h=$(lg HOME); g=$(lg GROK_HOME); p=$(lg PWD)
  [ -n "$h" ] && [ -n "$g" ] && [ -n "$p" ] || fail "missing dir(s): HOME=$h GROK_HOME=$g PWD=$p"
  [ "$h" != "$g" ] || fail "HOME == GROK_HOME ($h)"
  [ "$h" != "$p" ] || fail "HOME == CWD ($h)"
  [ "$g" != "$p" ] || fail "GROK_HOME == CWD ($g)"
  ok ;;

xtrace_redact)
  setup_home; export XAI_API_KEY="TRACE-SECRET-9x"
  ( set -x; grok_relay "hi" >/dev/null; ) 2>"$run/xtrace"; rc=$?
  grep -q 'TRACE-SECRET-9x' "$run/xtrace" && fail "SECURITY: key value traced to stderr under set -x"
  ok ;;

repo_tmpdir_abort)
  setup_home; mkdir -p "$run/repo"; ( cd "$run/repo" && git init -q ) || fail "git init"
  export TMPDIR="$run/repo/inside"; mkdir -p "$TMPDIR"
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 1 ] || fail "expected abort (exit 1), got $rc"
  grep -q 'git worktree' "$run/err" || fail "no git-worktree abort message: [$(cat "$run/err")]"
  fake_ran && fail "SECURITY: grok ran despite CWD inside a git repo"
  ok ;;

repo_tmpdir_symlink_abort)
  setup_home; mkdir -p "$run/realrepo/sub"; ( cd "$run/realrepo" && git init -q ) || fail "git init"
  ln -s "$run/realrepo/sub" "$run/linktmp"          # TMPDIR is a SYMLINK into a git repo subdir
  export TMPDIR="$run/linktmp"
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 1 ] || fail "expected abort via symlinked TMPDIR into repo, got $rc (err: $(cat "$run/err"))"
  grep -q 'git worktree' "$run/err" || fail "no git-worktree abort message: [$(cat "$run/err")]"
  fake_ran && fail "SECURITY: grok ran despite a symlinked TMPDIR resolving inside a git repo"
  ok ;;

mktemp_fail)
  setup_home; export TMPDIR="$run/no/such/dir"     # does not exist -> mktemp -d fails
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 1 ] || fail "expected exit 1 on mktemp failure, got $rc"
  fake_ran && fail "grok ran despite mktemp failure"
  ok ;;

config_write_fail)
  setup_home
  ( umask 0777; grok_relay "q" >/dev/null 2>"$run/err" ); rc=$?   # subdirs created mode 000 -> config write fails
  [ "$rc" = 1 ] || fail "expected exit 1 on config-write failure, got $rc (err: $(cat "$run/err"))"
  fake_ran && fail "grok ran despite config-write failure"
  ok ;;

brief_unreadable)
  setup_home; b=$(mkbrief image); chmod 000 "$b"
  grok_media "$b" "$run/out" >/dev/null 2>"$run/err"; rc=$?; chmod 644 "$b" 2>/dev/null
  [ "$rc" = 2 ] || fail "expected exit 2 on unreadable brief, got $rc"
  fake_ran && fail "grok ran despite unreadable brief"
  ok ;;

sub_default_auth)
  setup_home; unset GROK_AUTH_PATH
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc err=[$(cat "$run/err")]"
  [ "$(lg GROK_AUTH_PATH)" = "$realhome/.grok/auth.json" ] || fail "default auth path wrong: $(lg GROK_AUTH_PATH)"
  ok ;;

sub_custom_authpath)
  setup_home; mkdir -p "$run/c"; cp "$realhome/.grok/auth.json" "$run/c/auth.json"; export GROK_AUTH_PATH="$run/c/auth.json"
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc"
  [ "$(lg GROK_AUTH_PATH)" = "$run/c/auth.json" ] || fail "custom auth path not honored: $(lg GROK_AUTH_PATH)"
  ok ;;

sub_relative_auth)
  setup_home; mkdir -p "$run/rel/myauth"; cp "$realhome/.grok/auth.json" "$run/rel/myauth/auth.json"
  cd "$run/rel" || fail "cd"; export GROK_AUTH_PATH="myauth/auth.json"; want="$(pwd)/myauth/auth.json"
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 0 ] || fail "rc=$rc"
  [ "$(lg GROK_AUTH_PATH)" = "$want" ] || fail "relative auth not absolutised: $(lg GROK_AUTH_PATH) want $want"
  ok ;;

sub_failclosed)
  setup_home; rm -f "$realhome/.grok/auth.json"; unset GROK_AUTH_PATH
  grok_relay "q" >/dev/null 2>"$run/err"; rc=$?
  [ "$rc" = 1 ] || fail "expected fail-closed exit 1, got $rc"
  grep -q 'no readable auth' "$run/err" || fail "no fail-closed message: [$(cat "$run/err")]"
  fake_ran && fail "SECURITY: grok ran despite missing auth"
  ok ;;

