# Deliver INT/TERM/HUP to the helper's OWN main subshell (the parent of grok) — the process a
# foreground Ctrl-C reaches via the terminal's process group — and assert the helper's trap reaps its
# whole tree (grok + grandchild + watchdog) and removes its temp base. The helper runs in the
# FOREGROUND (so INT is not shell-ignored, unlike a backgrounded async job) while a background watcher
# sends exactly ONE signal to that subshell. The harness never kills grok or the grandchild itself —
# the helper's own trap does. (A backgrounded `grok_relay ... &` killed via $! instead hits the shell's
# intermediate wrapper subshell, not the trap; that path is covered by the watchdog timeout — see the
# `timeout` scenario and the LIMITATIONS note.)
_sigtest() {
  setup_home; printf 30 > "$LOGDIR/sleep"; printf 1 > "$LOGDIR/grandchild"; rm -f "$LOGDIR/fake.pid" "$LOGDIR/gc.pid"
  ( j=0; while [ "$j" -lt 300 ]; do { [ -s "$LOGDIR/fake.pid" ] && [ -s "$LOGDIR/gc.pid" ]; } && break; sleep 0.05; j=$((j+1)); done
    fp=$(cat "$LOGDIR/fake.pid" 2>/dev/null || true); [ -n "$fp" ] || exit 0
    s2=$(ps -o ppid= -p "$fp" 2>/dev/null | tr -d ' '); [ -n "$s2" ] && kill -"$_sig" "$s2" 2>/dev/null ) &
  watcher=$!
  grok_relay "long turn" >/dev/null 2>"$run/err"; rc=$?
  wait "$watcher" 2>/dev/null
  gc=$(cat "$LOGDIR/gc.pid" 2>/dev/null || true)
  i=0; while [ "$i" -lt 100 ]; do ls -d "$TMPDIR"/grok-ctl.* "$TMPDIR"/grok-sbx.* >/dev/null 2>&1 || break; sleep 0.05; i=$((i+1)); done
  ls -d "$TMPDIR"/grok-ctl.* "$TMPDIR"/grok-sbx.* >/dev/null 2>&1 && fail "leftover temp dir after $_sig (rc=$rc)"
  [ -n "$gc" ] && kill -0 "$gc" 2>/dev/null && { kill -9 "$gc" 2>/dev/null; fail "orphaned grandchild alive after $_sig"; }
  ok
}

# Deliver $_sig to the helper's trap-bearing subshell DURING the publish loop (after grok has exited).
# The helper runs in the FOREGROUND — exactly the case a real Ctrl-C hits — because a BACKGROUNDED shell
# job sets INT/QUIT to SIG_IGN, so INT to a `& job would be dropped (a test artifact, not a wrapper bug).
# A background watcher captures the trap-bearer PID = grok's PARENT (owned/parent-verified: grok's pid is
# written by our own fake) WHILE grok is alive, then signals THAT pid once publish has started. Never a
# process-name match, never grok itself. The trap must roll back THIS call's partials and, since it
# created OUTPUT-DIR, remove it when empty; and leave no control/sandbox base behind.
_pubsig() {
  setup_home; printf 1 > "$LOGDIR/sleep"; printf 120 > "$LOGDIR/imgcount"; rm -f "$LOGDIR/fake.pid"
  odir="$run/sig-$_sig-out"                                  # does NOT pre-exist -> out_pre=0
  ( tb=""; j=0
    while [ "$j" -lt 400 ]; do fp=$(cat "$LOGDIR/fake.pid" 2>/dev/null || true)
      [ -n "$fp" ] && { tb=$(ps -o ppid= -p "$fp" 2>/dev/null | tr -d ' '); [ -n "$tb" ] && break; }
      sleep 0.02; j=$((j+1)); done
    [ -n "$tb" ] || exit 0
    j=0; while [ "$j" -lt 1500 ]; do [ -n "$(find "$odir" -mindepth 1 2>/dev/null)" ] && break; sleep 0.02; j=$((j+1)); done
    kill -"$_sig" "$tb" 2>/dev/null ) &
  wp=$!
  grok_media "$(mkbrief image)" "$odir" >/dev/null 2>"$run/err"; rc=$?
  wait "$wp" 2>/dev/null; sleep 0.4
  # Real failures ALWAYS fail: the helper's control + sandbox temp bases must be gone.
  ls -d "$TMPDIR"/grok-ctl.* "$TMPDIR"/grok-sbx.* >/dev/null 2>&1 && fail "$_sig: leftover control/sandbox base after publish signal"
  # Classify OUTPUT-DIR residue against the accepted scope. A VISIBLE (published) artifact left behind =
  # rollback failure = FAIL. Only a HIDDEN .grokpub.* temp = the known local-only mktemp-assignment signal
  # window (a signal after mktemp creates the temp but before $curtmp is assigned): NOT external egress,
  # NOT user-data loss -> clean the exact test-owned residue and record a documented SKIP. No residue = PASS.
  if [ -d "$odir" ]; then
    vis=$(find "$odir" -type f ! -name '.grokpub.*' 2>/dev/null | wc -l | tr -d ' ')
    [ "${vis:-0}" = 0 ] || fail "$_sig: visible/published artifact left after signal during publish ($(ls "$odir"))"
    if [ "$(find "$odir" -type f -name '.grokpub.*' 2>/dev/null | wc -l | tr -d ' ')" != 0 ]; then
      find "$odir" -type f -name '.grokpub.*' -exec rm -f {} + 2>/dev/null   # clean the exact test-owned residue
      skip "known local-only mktemp assignment signal window; no external egress or source-data loss"
    fi
  fi
  ok
}
